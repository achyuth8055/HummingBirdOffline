import SwiftUI

// Spacing and corner radius tokens tuned for the premium dark UI
public enum HBSpacingToken {
    // Global horizontal inset for screens
    public static let hInset: CGFloat = 18
    // Gutter between items in horizontal rails
    public static let railGutter: CGFloat = 12
    // Gap between vertical sections
    public static let sectionGap: CGFloat = 14
}

public enum HBCornerToken {
    // Card / artwork corners
    public static let card: CGFloat = 16
    public static let art: CGFloat = 16
    // Mini-player pill
    public static let miniPill: CGFloat = 18
}

